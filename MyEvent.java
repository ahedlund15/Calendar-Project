import java.util.*;

public class MyEvent
{
  private String myName;
  private String myStart;
  private String myEnd;
  private String myDetails;
  public MyEvent(String eventName, String startEvent, String endEvent, String details)
  {
    myName = eventName;
    myStart = startEvent;
    myEnd = endEvent;
    myDetails = details;
  }
  
  
}